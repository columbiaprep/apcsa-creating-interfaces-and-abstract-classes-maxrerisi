public interface StatModifiers {
    private int strMod;
    private int dexMod;
    private int conMod;
    private int wisMod;
    private int chaMod;
    private int intMod;

    public int getStrMod() {
        return strMod;
    }

    public int getDexMod() {
        return dexMod;
    }

    public int getConMod() {
        return conMod;
    }

    public int getWisMod() {
        return wisMod;
    }

    public int getChaMod() {
        return chaMod;
    }

    public int getIntMod() {
        return intMod;
    }



}